class Data_Types:

  def __init__(self):
    pass

  def main():
    # This is an integer
    myInteger = 3

    print(type(myInteger))

    # This is called a float
    myFloat = 3.14

    print(type(myFloat))

    # This is an example of a string
    myString = "This is a string"

    print(type(myString))

    # This is a boolean
    myBool = False

    print(type(myBool))