class Arithmetic_Operators:

  def __init__(self):
    pass

  def main():
    # Addition
    x = 4
    y = 6

    sum = x + y
    print(sum)

    # Subtraction
    diff = y - x
    print(diff)

    # Multiplication
    prod = x * y
    print(prod)

    # Division
    quot = y / x
    print(quot)

    # Modulus
    mod = y % x
    print(mod)

    # Exponentiation
    exp = y**x
    print(exp)

    # Floor Division
    floor = y // x
    print(floor)
